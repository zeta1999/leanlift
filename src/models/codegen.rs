fn cpp(lts: &Lts) -> String {
    let sv = |s: &str| vid(s);
    let mut o = String::new();
    o.push_str("// AUTO-GENERATED by leanlift `lift model export --lang c++` — DO NOT EDIT.\n");
    o.push_str("#include <iostream>\n#include <optional>\n#include <sstream>\n#include <string>\n\n");

    o.push_str("enum class State {");
    o.push_str(&lts.states.iter().map(|s| sv(s)).collect::<Vec<_>>().join(", "));
    o.push_str("};\nenum class Event {");
    o.push_str(&lts.alphabet.iter().map(|e| sv(e)).collect::<Vec<_>>().join(", "));
    o.push_str("};\n\n");

    o.push_str("std::optional<State> step(State s, Event e) {\n    switch (s) {\n");
    for s in &lts.states {
        let arms: Vec<(String, String)> = lts
            .alphabet
            .iter()
            .filter_map(|e| lts.transitions.get(&(s.clone(), e.clone())).map(|t| (sv(e), sv(t))))
            .collect();
        if arms.is_empty() {
            continue;
        }
        o.push_str(&format!("    case State::{}: switch (e) {{\n", sv(s)));
        for (e, t) in arms {
            o.push_str(&format!("        case Event::{e}: return State::{t};\n"));
        }
        o.push_str("        default: return std::nullopt;\n    }\n");
    }
    o.push_str("    default: return std::nullopt;\n    }\n}\n\n");

    o.push_str("const char* name(State s) {\n    switch (s) {\n");
    for s in &lts.states {
        o.push_str(&format!("    case State::{}: return {:?};\n", sv(s), s));
    }
    o.push_str("    }\n    return \"?\";\n}\n\n");

    o.push_str("std::optional<Event> parse_event(const std::string& s) {\n");
    for e in &lts.alphabet {
        o.push_str(&format!("    if (s == {:?}) return Event::{};\n", e, sv(e)));
    }
    o.push_str("    return std::nullopt;\n}\n\n");

    o.push_str("bool forbidden(State s) {\n    switch (s) {\n");
    for f in &lts.forbid {
        o.push_str(&format!("    case State::{}: return true;\n", sv(f)));
    }
    o.push_str("    default: return false;\n    }\n}\n\n");

    o.push_str(&format!(
        "int main() {{\n\
        \x20   std::string line;\n\
        \x20   while (std::getline(std::cin, line)) {{\n\
        \x20       State s = State::{};\n\
        \x20       std::string out = name(s);\n\
        \x20       if (forbidden(s)) out += \" !\";\n\
        \x20       std::istringstream iss(line);\n\
        \x20       std::string tok;\n\
        \x20       while (iss >> tok) {{\n\
        \x20           auto e = parse_event(tok);\n\
        \x20           if (!e) {{ out += \" BADEVENT\"; break; }}\n\
        \x20           auto s2 = step(s, *e);\n\
        \x20           if (!s2) {{ out += \" BLOCKED\"; break; }}\n\
        \x20           s = *s2; out += \" \"; out += name(s);\n\
        \x20           if (forbidden(s)) out += \" !\";\n\
        \x20       }}\n\
        \x20       std::cout << out << \"\\n\";\n\
        \x20   }}\n\
        \x20   return 0;\n}}\n",
        sv(&lts.initial)
    ));
    o
}

