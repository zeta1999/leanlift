fn go(lts: &Lts) -> String {
    let sv = |s: &str| vid(s);
    let mut o = String::new();
    o.push_str("// AUTO-GENERATED by leanlift `lift model export --lang go` — DO NOT EDIT.\n");
    o.push_str("package main\n\nimport (\n\t\"bufio\"\n\t\"fmt\"\n\t\"os\"\n\t\"strings\"\n)\n\n");

    // States and events as int constants.
    o.push_str("type State int\ntype Event int\n\nconst (\n");
    for (i, s) in lts.states.iter().enumerate() {
        o.push_str(&format!("\tS_{} State = {}\n", sv(s), i));
    }
    o.push_str(")\nconst (\n");
    for (i, e) in lts.alphabet.iter().enumerate() {
        o.push_str(&format!("\tE_{} Event = {}\n", sv(e), i));
    }
    o.push_str(")\n\n");

    o.push_str("func step(s State, e Event) (State, bool) {\n\tswitch s {\n");
    for s in &lts.states {
        let arms: Vec<(String, String)> = lts
            .alphabet
            .iter()
            .filter_map(|e| lts.transitions.get(&(s.clone(), e.clone())).map(|t| (sv(e), sv(t))))
            .collect();
        if arms.is_empty() {
            continue;
        }
        o.push_str(&format!("\tcase S_{}:\n\t\tswitch e {{\n", sv(s)));
        for (e, t) in arms {
            o.push_str(&format!("\t\tcase E_{e}:\n\t\t\treturn S_{t}, true\n"));
        }
        o.push_str("\t\t}\n");
    }
    o.push_str("\t}\n\treturn s, false\n}\n\n");

    o.push_str("func name(s State) string {\n\tswitch s {\n");
    for s in &lts.states {
        o.push_str(&format!("\tcase S_{}:\n\t\treturn {:?}\n", sv(s), s));
    }
    o.push_str("\t}\n\treturn \"?\"\n}\n\n");

    o.push_str("func parseEvent(s string) (Event, bool) {\n\tswitch s {\n");
    for e in &lts.alphabet {
        o.push_str(&format!("\tcase {:?}:\n\t\treturn E_{}, true\n", e, sv(e)));
    }
    o.push_str("\t}\n\treturn 0, false\n}\n\n");

    o.push_str("func forbidden(s State) bool {\n\tswitch s {\n");
    for f in &lts.forbid {
        o.push_str(&format!("\tcase S_{}:\n\t\treturn true\n", sv(f)));
    }
    o.push_str("\t}\n\treturn false\n}\n\n");

    o.push_str(&format!(
        "func main() {{\n\
        \tsc := bufio.NewScanner(os.Stdin)\n\
        \tsc.Buffer(make([]byte, 1024*1024), 1024*1024)\n\
        \tfor sc.Scan() {{\n\
        \t\ts := S_{}\n\
        \t\tout := []string{{name(s)}}\n\
        \t\tif forbidden(s) {{ out = append(out, \"!\") }}\n\
        \t\tfor _, tok := range strings.Fields(sc.Text()) {{\n\
        \t\t\te, ok := parseEvent(tok)\n\
        \t\t\tif !ok {{ out = append(out, \"BADEVENT\"); break }}\n\
        \t\t\ts2, en := step(s, e)\n\
        \t\t\tif !en {{ out = append(out, \"BLOCKED\"); break }}\n\
        \t\t\ts = s2; out = append(out, name(s))\n\
        \t\t\tif forbidden(s) {{ out = append(out, \"!\") }}\n\
        \t\t}}\n\
        \t\tfmt.Println(strings.Join(out, \" \"))\n\
        \t}}\n}}\n",
        sv(&lts.initial)
    ));
    o
}
